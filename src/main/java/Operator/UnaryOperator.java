package Operator;

public class UnaryOperator 
{
	public static void main(String[] args) 
	{
		int a=10;
		System.out.println(+a);//positive number
		System.out.println(-a);//negative number
		a++;
		System.out.println(a);
		++a;
		System.out.println(a);
		--a;
		System.out.println(a);
		boolean press=false;
		System.out.println(press);
		System.out.println(!press);
		
	}

}
